import java.util.Date;

public class Medicine {
    int dose;
    String name;
    int idMedicine;
    Date schedule;

    public String getName() {
        return name;
    }

    public int getIdMedicine(){
        return idMedicine;
    }

    public Date getSchedule(){
        return schedule;
    }

    public int getDose() {
        return dose;
    }


}
